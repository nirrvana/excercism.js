export const decodedValue = (colors) => {
  let result = '';

  for (let index = 0; index < 2; index++) {
    result += COLORS.indexOf(colors[index]);
  }
  return Number(result);
};

const COLORS = [
  'black',
  'brown',
  'red',
  'orange',
  'yellow',
  'green',
  'blue',
  'violet',
  'grey',
  'white',
];
